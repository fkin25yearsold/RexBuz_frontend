// Helper function to get filtered case studies
export const getFilteredCaseStudies = (filters = {}) => {
  let filtered = [...caseStudiesData.case_studies];
  
  if (filters.size && filters.size.length > 0) {
    filtered = filtered.filter(study => filters.size.includes(study.size));
  }
  
  if (filters.category && filters.category.length > 0) {
    filtered = filtered.filter(study => filters.category.includes(study.category));
  }
  
  return filtered;
};

// API simulation functions for future backend integration
export const caseStudiesAPI = {
  getAllCaseStudies: (params = {}) => {
    return new Promise((resolve) => {
      setTimeout(() => {
        const { limit = 20, offset = 0, size, category } = params;
        let filtered = getFilteredCaseStudies({ size, category });
        
        const paginated = filtered.slice(offset, offset + limit);
        
        resolve({
          data: paginated,
          total: filtered.length,
          hasMore: offset + limit < filtered.length
        });
      }, 500);
    });
  },
  
  getCaseStudyBySlug: (slug) => {
    return new Promise((resolve, reject) => {
      setTimeout(() => {
        const study = caseStudiesData.case_studies.find(s => s.slug === slug);
        if (study) {
          resolve(study);
        } else {
          reject(new Error('Case study not found'));
        }
      }, 300);
    });
  },
  
  getFilters: () => {
    return new Promise((resolve) => {
      setTimeout(() => resolve(caseStudiesData.filters), 200);
    });
  },
  
  getStats: () => {
    return new Promise((resolve) => {
      setTimeout(() => resolve(caseStudiesData.stats), 200);
    });
  }
};
